package main

import "fmt"

func main() {
	fmt.Println("Materi tentang variable")
	//Penggunaan variabel dengan var dan tipe data
	var namaDepan string = "Anggiat"
	fmt.Println("Pengunaan variable namaDepan dengan var dan tipe data: ", namaDepan)
	//Penggunaan variable tanpa var dan tipe data
	namaBelakang := "Pangaribuan"
	fmt.Println("Penggunaan variable namaBelakang tanpa var dan tipe data: ", namaBelakang)

}
